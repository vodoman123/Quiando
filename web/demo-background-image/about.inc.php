<p>
	This demo shows you how to add images to a poll.
</p>

<p>
	You can always put an image outside of this poll folder and 
	refer to it by an absolute URL ( which starts with "http://" )
</p>

<p>
	If you want to keep the image in the folder
	to create a self-contained poll folder, then
	you need to know the URL to the folder.
</p>

<p>
	<span class='doc-kw'>getFolderUrl()</span> function returns
	just what you need. Read <span class='doc-kw'>tpl.front.inc.php</span>
	to see how it is used.
</p>
